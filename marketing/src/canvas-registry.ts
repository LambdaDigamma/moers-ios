// Canvas Registry - Maps device sizes to available Remotion compositions
// Each entry represents a canvas that can render screenshots for a specific device

import type { DeviceScreenshot } from "./apple-screenshot-sizes";

export interface CanvasEntry {
  id: string;
  deviceName: string;
  displayName: string;
  width: number;
  height: number;
  orientation: "portrait" | "landscape";
}

export interface CanvasRegistry {
  entries: CanvasEntry[];
}

const portraitOrient = "portrait" as const;
const landscapeOrient = "landscape" as const;

function createEntry(
  id: string,
  deviceName: string,
  displayName: string,
  width: number,
  height: number,
  orientation: "portrait" | "landscape",
): CanvasEntry {
  return { id, deviceName, displayName, width, height, orientation };
}

