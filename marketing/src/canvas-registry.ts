export function findCanvasByDevice(
  device: DeviceScreenshot,
  orientation: "portrait" | "landscape" = "portrait",
): CanvasEntry | undefined {
  return canvasRegistry.entries.find(
    (e) => e.deviceName === device.name && e.orientation === orientation,
  );
}

export function findCanvasByDimensions(
  width: number,
  height: number,
  orientation?: "portrait" | "landscape",
): CanvasEntry | undefined {
  return canvasRegistry.entries.find((e) => {
    if (orientation && e.orientation !== orientation) return false;
    return e.width === width && e.height === height;
  });
}

export function getAllCanvasEntries(): CanvasEntry[] {
  return canvasRegistry.entries;
}

export function getCanvasEntryById(id: string): CanvasEntry | undefined {
  return canvasRegistry.entries.find((e) => e.id === id);
}

export function getCanvasesByDeviceName(deviceName: string): CanvasEntry[] {
  return canvasRegistry.entries.filter((e) => e.deviceName === deviceName);
}

export function getCanvasesByOrientation(
  orientation: "portrait" | "landscape",
): CanvasEntry[] {
  return canvasRegistry.entries.filter((e) => e.orientation === orientation);
}
